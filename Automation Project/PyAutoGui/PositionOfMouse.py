import pyautogui 
import time


while(1) :
    time.sleep(0.3) # Intentional delay 
    print(pyautogui.position()) # Printing the position of mouse
    width , height = pyautogui.position() 
    if width == 0 or height ==0 : # Checking pause condition
        break # Stopping the program
# To stop move your mouse to one of two top corners 







