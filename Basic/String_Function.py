# Working with String

# String indexing starts with 0 
a = "Hello world !"
print(a[1]) # e

#Slicing String
b = "Hello World !"
print(b[2:5]) # llo

#Finding length of string
a = "Hello world !"
print(len(a))

#Converting all character into lowercase
a = "Hello world !"
print(a.lower())

#Converting all character into Uppercase
a = "Hello world !"
print(a.upper())

#Replacing substring with substring
a = "Hello world !"
print(a.replace("lo","l no . I'm Enguunbayar and this is my"))

#Searching substring in string 
a = "Hello world !"
print(a.find("!"))